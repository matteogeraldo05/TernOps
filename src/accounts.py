class Account:
    def __init__(self, name: str, password: str, isAdmin: bool):
        self.name = name
        self.password = password
        self.isAdmin = isAdmin
    
    def setName(name):
        self.name = name
    
    def getName():
        return self.name
    
    def setPassword(password):
        self.password = password
    
    def getPassword():
        return self.password
    
    def setIsAdmin(isAdmin):
        self.isAdmin = isAdmin

    def getIsAdmin():
        return self.isAdmin

